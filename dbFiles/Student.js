class Student{
    constructor(Reg,Name,DOB,Department,GPA,Address,PhoneNo){
        this.Reg = Reg;
        this.Name = Name;
        this.DOB = DOB;
        this.Department = Department;
        this.GPA = GPA;
        this.Address = Address;
        this.PhoneNo = PhoneNo;
    }
}

module.exports = Student;